! ============================================================================
! *** MODULE PARAMS <<<
! ***
! *** Provides all parameter functions which appear in the parabolic
! *** reaction diffusion equation
! ***
! ***     u_t + L_eps = f      on (0,1)x(0,1]
! ***     u(0,t) = gamma_0   , u(1,t) = gamma_1   for all t,
! ***     u(x,0) = u_0                            for all x,
! ***
! *** where
! ***
! ***     L_eps = -eps u_xx + b(x) u .
! ***
! ============================================================================
module PARAMS

  USE KINDMOD

  implicit none

  ! strict lower bound on b
  real(RNP),parameter :: rho = 1.0_RNP,  & !strict lower bound on b
                         x0  = 0.0_RNP,  &
                         x1  = 1.0_RNP,  &
                         T1  = 1.0_RNP

  contains

! ----------------------------------------------------------------------------
! *** FUNCTION c(x) <<<
! ***
! ----------------------------------------------------------------------------
real(RNP) function c(x)

  !arguments
  real(RNP), intent(in) :: x

  c = 1.0_RNP

end function c
! ---------------------------------- ------------------------------------------
! *** END of FUNCTION c >>>
! ----------------------------------------------------------------------------

! ----------------------------------------------------------------------------
! *** FUNCTION f(t) <<<
! ***
! ----------------------------------------------------------------------------
real(RNP) function f(x,t,eps)

  !arguments
  real(RNP), intent(in) :: x,t,eps

  !locals
  real(RNP) :: rhoeps

  rhoeps = sqrt(rho)/sqrt(eps)

  f = (&
          c(x)* (1.0_RNP-exp(-x*rhoeps))*(1.0_RNP-exp(-(1.0_RNP-x)*rhoeps)) &
         + rho* ( exp(-x*rhoeps) + exp(-(1.0_RNP-x)*rhoeps) ) &
      )/ (1.0_RNP-exp(-rhoeps))

end function f
! ----------------------------------------------------------------------------
! *** END of FUNCTION f >>>
! ----------------------------------------------------------------------------

! ----------------------------------------------------------------------------
! *** function u0 <<<
! ***
! *** u_0(x)
! ***
! ----------------------------------------------------------------------------
real(RNP) function u0(x,eps)

  !arguments
  real(RNP),intent(in) :: x,eps

  !locals
  real(RNP) :: rhoeps

  rhoeps = sqrt(rho)/sqrt(eps)

  u0 = (1.0_RNP-exp(-x*rhoeps))*(1.0_RNP-exp(-(1.0_RNP-x)*rhoeps)) &
     / (1.0_RNP-exp(-rhoeps))

end function u0
! ----------------------------------------------------------------------------
! *** END of function u0 >>>
! ----------------------------------------------------------------------------

! ----------------------------------------------------------------------------
! *** FUNCTION gamma0 <<<
! ***
! ----------------------------------------------------------------------------
real(RNP) function gamma0(t)

  !arguments
  real(RNP), intent(in)  :: t

  gamma0 = 0.0_RNP

end function gamma0
! ----------------------------------------------------------------------------
! *** END of FUNCTION gamma0 >>>
! ----------------------------------------------------------------------------

! ----------------------------------------------------------------------------
! *** FUNCTION gamma1 <<<
! ***
! ----------------------------------------------------------------------------
real(RNP) function gamma1(t)

  !arguments
  real(RNP), intent(in)  :: t

  gamma1 = 0.0_RNP

end function gamma1
! ----------------------------------------------------------------------------
! *** END of FUNCTION gamma1 >>>
! ----------------------------------------------------------------------------

! ----------------------------------------------------------------------------
! *** FUNCTION solution(x,t) <<<
! ***
! ----------------------------------------------------------------------------
real(RNP) function solution(x,t,eps)

  !arguments
  real(RNP), intent(in) :: x,t,eps

  !locals
  real(RNP) :: rhoeps

  rhoeps = sqrt(rho)/sqrt(eps)

  solution = (1.0_RNP-exp(-x*rhoeps))*(1.0_RNP-exp(-(1.0_RNP-x)*rhoeps)) &
           / (1.0_RNP-exp(-rhoeps))

end function solution
! ----------------------------------------------------------------------------
! *** END of FUNCTION solution >>>
! ----------------------------------------------------------------------------

end module PARAMS
! ============================================================================
! *** END of MODULE PARAMS >>>
